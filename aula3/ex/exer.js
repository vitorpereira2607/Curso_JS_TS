/* 
    In the first console.log(), everything between the parentheses is a single string. 
    In the second console.log(), the elements separated by commas are treated individually, so the number 14 is not part of the string; it's displayed as a separate number.
*/
console.log('Meu nome é "Vitor". Estou aprendendo JavaScript às 14 da tarde');
console.log('Meu nome é "Vitor". Estou aprendendo JavaScript às', 14, 'da tarde');
